import { Component, OnInit } from '@angular/core';
import { HttpClient } from '@angular/common/http';
import { CommonModule } from '@angular/common';
import { Router, RouterLink } from '@angular/router';
import { HttpClientModule } from '@angular/common/http';
import { DocenteChat } from './docente-chat/docente-chat';
import { DocenteNotificaciones } from './docente-notificaciones/docente-notificaciones';

@Component({
  selector: 'app-docente',
  standalone: true,
  imports: [CommonModule, RouterLink, HttpClientModule, DocenteChat, DocenteNotificaciones],
  templateUrl: './docente.html',
  styleUrls: ['./docente.css'],
})
export class Docente implements OnInit {
  secciones: any[] = [];
  alumnos: any[] = [];
  showUserMenu = false;
  showChat = false;
  showAlumnosPanel = false;
  unreadMessages = 0;
  isLoading = true;
  isLoadingAlumnos = false;
  showNotificaciones = false;
  totalNotificaciones = 0;
  errorMessage: string = '';
  alumnosErrorMessage: string = '';
  
  // üü¢ ESTRUCTURA UNIFICADA - Usa campos consistentes
  currentUser: any = {
    id_docente: null,
    id_usuario: null,
    nombre: '',
    apellido: '',
    correo: '', // üü¢ Usar solo 'correo' para consistencia
    rol: ''
  };

  constructor(private http: HttpClient, private router: Router) {}

  ngOnInit(): void {
    console.log('üéØ Inicializando panel docente...');
    this.loadCurrentUser();
  // üÜï Cargar contador de notificaciones
    setTimeout(() => this.actualizarContadorNotificaciones(), 1000);
  }
  

  // üü¢ M√âTODO MEJORADO: Cargar usuario con datos consistentes
  loadCurrentUser(): void {
    console.log('üîç Cargando informaci√≥n del usuario...');
    
    const userData = localStorage.getItem('currentUser');
    const token = localStorage.getItem('token');
    
    if (!userData || !token) {
      this.errorMessage = 'No est√°s autenticado. Por favor inicia sesi√≥n.';
      this.isLoading = false;
      this.router.navigate(['/login']);
      return;
    }
    

    try {
      const parsedUser = JSON.parse(userData);
      
      // üü¢ MAPEO CONSISTENTE: Usar siempre los mismos campos
      this.currentUser = {
        id_docente: parsedUser.id_docente || null,
        id_usuario: parsedUser.id_usuario || parsedUser.id || null,
        nombre: parsedUser.nombre || parsedUser.nombres || '',
        apellido: parsedUser.apellido || parsedUser.apellidos || '',
        correo: parsedUser.correo || parsedUser.email || '', // üü¢ Unificar en 'correo'
        rol: parsedUser.rol || 'docente'
      };
      
      console.log('‚úÖ Usuario cargado y normalizado:', this.currentUser);
      
      // üü¢ ESTRATEGIA MEJORADA: Obtener datos frescos del backend
      this.obtenerDatosDocenteCompletos();
      
    } catch (error) {
      console.error('‚ùå Error al procesar datos del usuario:', error);
      this.errorMessage = 'Error al cargar datos del usuario';
      this.isLoading = false;
    }
  }

  // üü¢ NUEVO M√âTODO: Obtener datos completos y actualizados del docente
  obtenerDatosDocenteCompletos(): void {
    console.log('üîÑ Obteniendo datos actualizados del docente...');
    
    this.http.get<any>('http://localhost:4000/api/docentes/mi-docente', {
      headers: {
        'Authorization': `Bearer ${localStorage.getItem('token')}`
      }
    }).subscribe({
      next: (response) => {
        console.log('‚úÖ Datos del docente obtenidos:', response);
        
        // üü¢ ACTUALIZAR CON DATOS FRESCOS DEL BACKEND
        if (response.id_docente) {
          this.currentUser.id_docente = response.id_docente;
          
          // Si el backend devuelve m√°s informaci√≥n del docente, actualizarla
          if (response.docente) {
            this.currentUser = {
              ...this.currentUser,
              ...response.docente,
              correo: response.docente.correo || this.currentUser.correo
            };
          }
          
          console.log('‚úÖ Datos del docente actualizados:', this.currentUser);
          this.loadSecciones();
        } else {
          this.manejarError('No se pudo identificar tu perfil de docente');
        }
      },
      error: (error) => {
        console.error('‚ùå Error al obtener datos del docente:', error);
        
        if (error.status === 401) {
          this.manejarError('Sesi√≥n expirada. Por favor inicia sesi√≥n nuevamente.', true);
        } else if (error.status === 404) {
          console.warn('‚ö†Ô∏è Servicio docente no disponible, usando datos locales');
          this.loadSecciones(); // Intentar con datos locales
        } else {
          this.manejarError('Error al conectar con el servidor');
        }
      }
    });
  }

  // üü¢ M√âTODO MEJORADO: Cargar secciones con manejo robusto de errores
  loadSecciones(): void {
    const docenteId = this.currentUser?.id_docente;
    
    if (!docenteId) {
      this.manejarError('No se pudo identificar al docente para cargar las secciones');
      return;
    }

    console.log('üîÑ Cargando secciones para docente ID:', docenteId);
    
    const url = `http://localhost:4000/api/secciones/docente/${docenteId}`;
    
    this.isLoading = true;
    this.errorMessage = '';
    
    this.http.get<any>(url, {
      headers: {
        'Authorization': `Bearer ${localStorage.getItem('token')}`
      }
    }).subscribe({
      next: (response) => {
        this.isLoading = false;
        
        if (response.success && Array.isArray(response.data)) {
          this.secciones = this.procesarSecciones(response.data);
          console.log(`‚úÖ ${this.secciones.length} secciones cargadas correctamente`);
          
          if (this.secciones.length === 0) {
            this.errorMessage = 'No tienes secciones asignadas. Contacta al administrador.';
          }
        } else {
          this.manejarError('Formato de respuesta inv√°lido del servidor');
        }
      },
      error: (error) => {
        this.isLoading = false;
        console.error('‚ùå Error cargando secciones:', error);
        this.manejarErrorCargaSecciones(error);
      }
    });
  }

  // üü¢ NUEVO M√âTODO: Procesar y normalizar datos de secciones
  private procesarSecciones(secciones: any[]): any[] {
    return secciones.map(seccion => ({
      ...seccion,
      // üü¢ Asegurar campos consistentes
      nombre: seccion.nombre || 'Sin nombre',
      curso: seccion.curso || seccion.nombre_curso || 'Sin curso asignado',
      estudiantes_count: seccion._count?.estudiantes || 0,
      actividades_count: seccion._count?.actividades || 0
    }));
  }

  // üü¢ M√âTODO MEJORADO: Cargar alumnos con datos consistentes
  loadAlumnos(id_seccion?: number): void {
    if (!this.currentUser?.id_docente) {
      this.alumnosErrorMessage = 'No se pudo identificar al docente.';
      return;
    }

    this.isLoadingAlumnos = true;
    this.alumnosErrorMessage = '';
    
    const url = id_seccion 
      ? `http://localhost:4000/api/secciones/docente/${this.currentUser.id_docente}/alumnos/seccion/${id_seccion}`
      : `http://localhost:4000/api/secciones/docente/${this.currentUser.id_docente}/alumnos`;

    console.log('üë• Cargando alumnos desde:', url);
    
    this.http.get<any>(url, {
      headers: {
        'Authorization': `Bearer ${localStorage.getItem('token')}`
      }
    }).subscribe({
      next: (response) => {
        this.isLoadingAlumnos = false;
        
        if (response.success && Array.isArray(response.data)) {
          this.alumnos = this.procesarAlumnos(response.data);
          this.showAlumnosPanel = true;
          console.log(`‚úÖ ${this.alumnos.length} alumnos cargados correctamente`);
        } else {
          this.alumnosErrorMessage = 'No se pudieron cargar los alumnos';
          this.alumnos = [];
        }
      },
      error: (error) => {
        this.isLoadingAlumnos = false;
        console.error('‚ùå Error cargando alumnos:', error);
        this.alumnosErrorMessage = 'Error al cargar los alumnos';
        this.alumnos = [];
      }
    });
  }

  // üü¢ NUEVO M√âTODO: Procesar y normalizar datos de alumnos
  private procesarAlumnos(alumnos: any[]): any[] {
    return alumnos.map(alumno => ({
      ...alumno,
      // üü¢ Campos consistentes para alumnos
      nombre_completo: `${alumno.nombre || ''} ${alumno.apellido || ''}`.trim(),
      correo: alumno.correo || alumno.email || '',
      tiene_chat: alumno.tieneChat || false
    }));
  }

  // üü¢ M√âTODO MEJORADO: Manejo centralizado de errores
  private manejarError(mensaje: string, redirigir: boolean = false): void {
    console.error('‚ùå Error:', mensaje);
    this.errorMessage = mensaje;
    this.isLoading = false;
    
    if (redirigir) {
      setTimeout(() => this.router.navigate(['/login']), 2000);
    }
  }

  // üü¢ M√âTODO MEJORADO: Manejo espec√≠fico de errores de secciones
  private manejarErrorCargaSecciones(error: any): void {
    let mensaje = 'Error al cargar las secciones';
    
    switch (error.status) {
      case 401:
        mensaje = 'Sesi√≥n expirada. Por favor inicia sesi√≥n nuevamente.';
        setTimeout(() => this.router.navigate(['/login']), 2000);
        break;
      case 404:
        mensaje = 'No se encontraron secciones para este docente.';
        break;
      case 500:
        mensaje = 'Error interno del servidor. Intenta nuevamente.';
        break;
      case 0:
        mensaje = 'No se puede conectar al servidor. Verifica tu conexi√≥n.';
        break;
      default:
        mensaje = `Error: ${error.message}`;
    }
    
    this.manejarError(mensaje);
    this.secciones = [];
  }

  // ‚úÖ M√âTODOS DE UI MEJORADOS
  verAlumnosSeccion(seccion: any): void {
    console.log('üë• Viendo alumnos de la secci√≥n:', seccion.nombre);
    this.loadAlumnos(seccion.id_seccion);
  }

  verTodosLosAlumnos(): void {
    console.log('üë• Viendo todos los alumnos del docente');
    this.loadAlumnos();
  }

  cerrarPanelAlumnos(): void {
    this.showAlumnosPanel = false;
    this.alumnos = [];
    this.alumnosErrorMessage = '';
  }

  refreshSecciones(): void {
    console.log('üîÑ Actualizando secciones...');
    this.loadSecciones();
  }

  toggleUserMenu(): void {
    this.showUserMenu = !this.showUserMenu;
  }

  toggleChat(): void {
    this.showChat = !this.showChat;
    if (this.showChat) {
      this.unreadMessages = 0;
    }
  }

  closeChat(): void {
    this.showChat = false;
  }

    // üÜï M√©todo para toggle de notificaciones
  toggleNotificaciones(): void {
    this.showNotificaciones = !this.showNotificaciones;
  }

  cerrarNotificaciones(): void {
    this.showNotificaciones = false;
  }

  // üÜï Actualizar el contador de notificaciones
  // üÜï M√âTODO MEJORADO CON DIAGN√ìSTICO
actualizarContadorNotificaciones(): void {
  if (!this.currentUser?.id_docente) {
    console.warn('‚ö†Ô∏è No hay ID de docente para cargar notificaciones');
    this.totalNotificaciones = 0;
    return;
  }

  const docenteId = this.currentUser.id_docente;
  
  console.log('üîÑ Solicitando notificaciones para docente ID:', docenteId);
  console.log('üë§ Docente actual:', this.getUserDisplayName());
  
  this.http.get<any>(`http://localhost:4000/api/notificaciones/docente/${docenteId}`, {
    headers: {
      'Authorization': `Bearer ${localStorage.getItem('token')}`
    }
  }).subscribe({
    next: (response) => {
      console.log('‚úÖ Respuesta completa de notificaciones:', response);
      
      if (response.success && Array.isArray(response.data)) {
        this.totalNotificaciones = response.data.length;
        console.log(`üì¢ Docente "${this.getUserDisplayName()}" tiene ${this.totalNotificaciones} notificaciones`);
        
        // Mostrar detalles de las notificaciones
        response.data.forEach((notif: any, index: number) => {
          console.log(`   ${index + 1}. ${notif.mensaje} (${notif.tipo})`);
        });
      } else {
        console.warn('‚ö†Ô∏è Formato de respuesta inesperado:', response);
        this.totalNotificaciones = 0;
      }
    },
    error: (error) => {
      console.error('‚ùå Error al obtener notificaciones:', error);
      
      if (error.status === 404) {
        console.error('üî¥ ENDPOINT NO ENCONTRADO: Verifica que hayas agregado las rutas en app.js');
        console.log('üí° Soluci√≥n: Agrega estas l√≠neas en app.js:');
        console.log('   import notificacionesRoutes from "./routes/notificaciones.routes.js"');
        console.log('   app.use("/api/notificaciones", authMiddleware, notificacionesRoutes)');
      }
      
      this.totalNotificaciones = 0;
    }
  });
}

  onSeccionesActualizadas(secciones: any[]): void {
    console.log('Secciones actualizadas desde el chat:', secciones);
    this.secciones = this.procesarSecciones(secciones);
  }

  logout(): void {
    console.log('üö™ Cerrando sesi√≥n...');
    localStorage.removeItem('token');
    localStorage.removeItem('currentUser');
    this.router.navigate(['/login']);
  }

  // ‚úÖ M√âTODOS DE DATOS MEJORADOS
  getTotalEstudiantes(): number {
    return this.secciones.reduce((total, seccion) => 
      total + (seccion.estudiantes_count || 0), 0
    );
  }

  getTotalActividades(): number {
    return this.secciones.reduce((total, seccion) => 
      total + (seccion.actividades_count || 0), 0
    );
  }

  // üü¢ M√âTODOS DE USUARIO MEJORADOS - Campos consistentes
  getUserDisplayName(): string {
    const nombre = this.currentUser.nombre?.trim();
    const apellido = this.currentUser.apellido?.trim();
    
    if (nombre && apellido) {
      return `${nombre} ${apellido}`;
    } else if (nombre) {
      return nombre;
    } else if (apellido) {
      return apellido;
    }
    
    return 'Docente';
  }

  getUserEmail(): string {
    return this.currentUser.correo || 'correo@ejemplo.com';
  }

  // ‚úÖ M√âTODOS DE RENDIMIENTO
  trackBySeccionId(index: number, seccion: any): number {
    return seccion.id_seccion;
  }

  trackByAlumnoId(index: number, alumno: any): number {
    return alumno.id_estudiante || index;
  }

  // üîç M√âTODOS DE DIAGN√ìSTICO MEJORADOS
  testBackendCompleto(): void {
    console.group('üß™ TEST COMPLETO DEL SISTEMA');
    
    const tests = [
      { name: 'Health Check', url: 'http://localhost:4000/api/secciones/health' },
      { name: 'Servicio Docente', url: 'http://localhost:4000/api/docentes/mi-docente' },
      { name: 'Secciones', url: `http://localhost:4000/api/secciones/docente/${this.currentUser.id_docente || 1}` }
    ];
    
    tests.forEach(test => {
      this.http.get(test.url, {
        headers: { 'Authorization': `Bearer ${localStorage.getItem('token')}` }
      }).subscribe({
        next: (response) => console.log(`‚úÖ ${test.name}: OK`),
        error: (error) => console.log(`‚ùå ${test.name}: FALLA (${error.status})`)
      });
    });
    
    console.groupEnd();
  }

  // üü¢ NUEVO M√âTODO: Verificar estado del sistema
  verificarEstadoSistema(): void {
    console.group('üîç ESTADO DEL SISTEMA');
    console.log('üë§ Usuario:', this.getUserDisplayName());
    console.log('üìß Correo:', this.getUserEmail());
    console.log('üéØ ID Docente:', this.currentUser.id_docente);
    console.log('üìö Secciones:', this.secciones.length);
    console.log('üë• Alumnos cargados:', this.alumnos.length);
    console.log('üîê Autenticado:', !!localStorage.getItem('token'));
    console.groupEnd();
  }
}